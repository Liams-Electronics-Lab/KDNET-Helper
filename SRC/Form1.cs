        private bool CreateRestorePoint(string description)
        {
            try
            {
                using (var managementClass = new ManagementClass("\\\\.\\root\\default", "SystemRestore", null))
                {
                    var results = managementClass.GetMethodParameters("CreateRestorePoint");
                    results["Description"] = description;
                    results["RestorePointType"] = MODIFY_SETTINGS;
                    results["EventType"] = BEGIN_SYSTEM_CHANGE;
                    var outParams = managementClass.InvokeMethod("CreateRestorePoint", results, null);
                    return (uint)outParams["ReturnValue"] == 0;
                }
            }
            catch
            {
                try
                {
                    var psi = new ProcessStartInfo
                    {
                        FileName = "powershell",
                        Arguments = $"-Command \"Checkpoint-Computer -Description '{description}' -RestorePointType MODIFY_SETTINGS\"",
                        UseShellExecute = false,
                        CreateNoWindow = true
                    };
                    using (var proc = Process.Start(psi))
                    {
                        proc.WaitForExit();
                        return proc.ExitCode == 0;
                    }
                }
                catch
                {
                    return false;
                }
            }
        }

