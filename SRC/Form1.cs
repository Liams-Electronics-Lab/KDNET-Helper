        private void LoadCurrentDebugSettings()
        {
            AppendLog("Target", "Loading current debug settings...");
            RunCommandAndLog("bcdedit", "/dbgsettings", out string output, out string error);
            if (!string.IsNullOrEmpty(output))
            {
                var lines = output.Split(new[] { "\r\n", "\n" }, StringSplitOptions.RemoveEmptyEntries);
                foreach (var line in lines)
                {
                    if (line.Trim().StartsWith("key", StringComparison.OrdinalIgnoreCase))
                    {
                        var parts = line.Split(new[] { ' ' }, StringSplitOptions.RemoveEmptyEntries);
                        if (parts.Length >= 2)
                        {
                            string key = parts[1];
                            txtGeneratedKey.Text = key;
                            AppendLog("Target", $"Found existing key: {key}");
                            break;
                        }
                    }
                }
            }
            else if (!string.IsNullOrEmpty(error))
            {
                AppendLog("Target", "Could not read debug settings: " + error);
            }
        }

