        private void ScrollToBottom(RichTextBox richTextBox)
        {
            if (richTextBox.InvokeRequired)
            {
                richTextBox.Invoke(new Action(() => ScrollToBottom(richTextBox)));
                return;
            }
            richTextBox.SelectionStart = richTextBox.TextLength;
            richTextBox.ScrollToCaret();
            richTextBox.Refresh();
        }

        private void AppendLog(string tab, string message)
        {
            if (tab == "Target" && txtTargetLog.InvokeRequired)
            {
                txtTargetLog.Invoke(new Action(() => AppendLog(tab, message)));
                return;
            }
            if (tab == "Host" && txtHostLog.InvokeRequired)
            {
                txtHostLog.Invoke(new Action(() => AppendLog(tab, message)));
                return;
            }
            string timestamp = DateTime.Now.ToString("HH:mm:ss");
            string line = $"[{timestamp}] {message}{Environment.NewLine}";
            if (tab == "Target")
            {
                txtTargetLog.AppendText(line);
                if (chkTargetAutoScroll != null && chkTargetAutoScroll.Checked)
                    ScrollToBottom(txtTargetLog);
            }
            else if (tab == "Host")
            {
                txtHostLog.AppendText(line);
                if (chkHostAutoScroll != null && chkHostAutoScroll.Checked)
                    ScrollToBottom(txtHostLog);
            }
        }

