        private void RunCommandAndLog(string command, string arguments, out string output, out string error)
        {
            output = "";
            error = "";
            try
            {
                var psi = new ProcessStartInfo
                {
                    FileName = command,
                    Arguments = arguments,
                    RedirectStandardOutput = true,
                    RedirectStandardError = true,
                    UseShellExecute = false,
                    CreateNoWindow = true
                };
                using (var proc = Process.Start(psi))
                {
                    output = proc.StandardOutput.ReadToEnd();
                    error = proc.StandardError.ReadToEnd();
                    proc.WaitForExit();
                }
            }
            catch (Exception ex)
            {
                error = ex.Message;
            }
        }

        private void RunCommandWithTimeout(string command, string arguments, int timeoutMilliseconds)
        {
            var psi = new ProcessStartInfo
            {
                FileName = command,
                Arguments = arguments,
                RedirectStandardOutput = true,
                RedirectStandardError = true,
                UseShellExecute = false,
                CreateNoWindow = true
            };
            using (var proc = Process.Start(psi))
            {
                if (proc != null)
                {
                    if (!proc.WaitForExit(timeoutMilliseconds))
                    {
                        proc.Kill();
                        throw new TimeoutException($"Command '{command} {arguments}' timed out after {timeoutMilliseconds} ms");
                    }
                    string error = proc.StandardError.ReadToEnd();
                    if (!string.IsNullOrEmpty(error))
                        AppendLog("Target", $"Command warning: {error}");
                }
            }
        }

        private string GetLocalIPv4()
        {
            if (cmbTargetIPs.SelectedItem != null && cmbTargetIPs.SelectedItem.ToString() != "No IPv4 address")
                return cmbTargetIPs.SelectedItem.ToString();
            var ip = NetworkInterface.GetAllNetworkInterfaces()
                .Where(nic => nic.OperationalStatus == OperationalStatus.Up && nic.NetworkInterfaceType != NetworkInterfaceType.Loopback)
                .SelectMany(nic => nic.GetIPProperties().UnicastAddresses)
                .FirstOrDefault(addr => addr.Address.AddressFamily == System.Net.Sockets.AddressFamily.InterNetwork);
            return ip?.Address.ToString() ?? "unknown";
        }

        private bool IsShareReady()
        {
            if (!Directory.Exists(SHARE_FOLDER)) return false;
            if (!File.Exists(Path.Combine(SHARE_FOLDER, INI_FILE))) return false;
            var psi = new ProcessStartInfo
            {
                FileName = "net",
                Arguments = "share",
                RedirectStandardOutput = true,
                UseShellExecute = false,
                CreateNoWindow = true
            };
            using (var proc = Process.Start(psi))
            {
                string output = proc.StandardOutput.ReadToEnd();
                if (!output.Contains(SHARE_NAME)) return false;
            }
            psi = new ProcessStartInfo
            {
                FileName = "net",
                Arguments = $"user {SHARE_USER}",
                RedirectStandardOutput = true,
                UseShellExecute = false,
                CreateNoWindow = true
            };
            using (var proc = Process.Start(psi))
            {
                string output = proc.StandardOutput.ReadToEnd();
                if (!output.Contains(SHARE_USER)) return false;
            }
            string iniPath = Path.Combine(SHARE_FOLDER, INI_FILE);
            if (File.Exists(iniPath))
            {
                var lines = File.ReadAllLines(iniPath);
                foreach (var line in lines)
                {
                    if (line.StartsWith("Key="))
                    {
                        string savedKey = line.Substring(4);
                        if (savedKey == txtGeneratedKey.Text)
                            return true;
                    }
                }
            }
            return false;
        }

        private async Task CleanupShareAsync()
        {
            AppendLog("Target", "Cleaning up network share...");
            await Task.Run(() => RunCommandWithTimeout("schtasks", "/delete /tn \"KDNETHELPER_Cleanup\" /f", 10000));
            await Task.Run(() => RunCommandWithTimeout("net", $"share {SHARE_NAME} /delete", 10000));
            if (Directory.Exists(SHARE_FOLDER))
            {
                try { Directory.Delete(SHARE_FOLDER, true); }
                catch (Exception ex) { AppendLog("Target", $"Failed to delete folder: {ex.Message}"); }
            }
            await Task.Run(() => RunCommandWithTimeout("net", $"user {SHARE_USER} /delete", 10000));
            AppendLog("Target", "Cleanup completed.");
        }

        private async Task CreateShareAsync()
        {
            AppendLog("Target", "Setting up network share for key...");
            try
            {
                await Task.Run(() => RunCommandWithTimeout("net", $"user {SHARE_USER} {SHARE_PASS} /add /fullname:\"KDNET Helper\"", 10000));
                if (!Directory.Exists(SHARE_FOLDER))
                    Directory.CreateDirectory(SHARE_FOLDER);
                string iniPath = Path.Combine(SHARE_FOLDER, INI_FILE);
                await File.WriteAllTextAsync(iniPath, $"MachineName={Environment.MachineName}\r\nKey={txtGeneratedKey.Text}\r\nCreated={DateTime.Now}");
                await Task.Run(() => RunCommandWithTimeout("icacls", $"{SHARE_FOLDER} /grant {SHARE_USER}:(OI)(CI)F /T", 30000));
                await Task.Run(() => RunCommandWithTimeout("net", $"share {SHARE_NAME}={SHARE_FOLDER} /GRANT:{SHARE_USER},FULL", 10000));
                
                // PowerShell one-liner scheduled task for cleanup
                string psCommand = $@"
    $logFile = '{SHARE_FOLDER}\\cleanup.log'
    Add-Content $logFile -Value ""$(Get-Date) - Cleaning up KDNETHELPER share...""
    try {{
        net share {SHARE_NAME} /delete >> $logFile 2>&1
        Remove-Item -Path '{SHARE_FOLDER}' -Recurse -Force -ErrorAction SilentlyContinue
        net user {SHARE_USER} /delete >> $logFile 2>&1
        schtasks /delete /tn ""KDNETHELPER_Cleanup"" /f >> $logFile 2>&1
        Add-Content $logFile -Value ""$(Get-Date) - Cleanup completed successfully.""
    }} catch {{
        Add-Content $logFile -Value ""$(Get-Date) - Error: $_""
    }}
";
                psCommand = psCommand.Replace("\"", "\\\"");
                string taskArgs = $"-Command \"{psCommand}\"";
                string createTaskCmd = $"/create /tn \"KDNETHELPER_Cleanup\" /tr \"powershell.exe -ExecutionPolicy Bypass {taskArgs}\" /sc ONSTART /ru SYSTEM /f";
                await Task.Run(() => RunCommandWithTimeout("schtasks", createTaskCmd, 10000));
                
                AppendLog("Target", "Share created successfully. Cleanup scheduled on next boot (logs: " + SHARE_FOLDER + "\\cleanup.log).");
                MessageBox.Show($"Network share created successfully.\n\nFolder: {SHARE_FOLDER}\nShare: \\\\{GetLocalIPv4()}\\{SHARE_NAME}\nUser: {SHARE_USER}\n\nThe key will be automatically removed on next system restart.", "Share Created", MessageBoxButtons.OK, MessageBoxIcon.Information);
            }
            catch (Exception ex)
            {
                AppendLog("Target", $"Error creating share: {ex.Message}");
                MessageBox.Show($"Failed to create share: {ex.Message}", "Share Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                throw;
            }
        }

        private void PopulateTargetIPs()
        {
            cmbTargetIPs.Items.Clear();
            var ips = NetworkInterface.GetAllNetworkInterfaces()
                .Where(nic => nic.OperationalStatus == OperationalStatus.Up && nic.NetworkInterfaceType != NetworkInterfaceType.Loopback)
                .SelectMany(nic => nic.GetIPProperties().UnicastAddresses)
                .Where(addr => addr.Address.AddressFamily == System.Net.Sockets.AddressFamily.InterNetwork)
                .Select(addr => addr.Address.ToString())
                .Distinct()
                .ToList();
            if (ips.Count == 0)
                cmbTargetIPs.Items.Add("No IPv4 address");
            else
                cmbTargetIPs.Items.AddRange(ips.ToArray());
            if (cmbTargetIPs.Items.Count > 0)
                cmbTargetIPs.SelectedIndex = 0;
        }

