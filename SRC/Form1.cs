        private string ReadKeyFromTargetShare(string targetIP)
        {
            string uncPath = $@"\\{targetIP}\{SHARE_NAME}\{INI_FILE}";
            string localFile = Path.Combine(Path.GetTempPath(), Guid.NewGuid().ToString() + ".ini");
            try
            {
                var psi = new ProcessStartInfo
                {
                    FileName = "net",
                    Arguments = $"use \\\\{targetIP}\\{SHARE_NAME} /user:{SHARE_USER} {SHARE_PASS}",
                    RedirectStandardOutput = true,
                    RedirectStandardError = true,
                    UseShellExecute = false,
                    CreateNoWindow = true
                };
                using (var proc = Process.Start(psi))
                {
                    proc.WaitForExit();
                    if (proc.ExitCode != 0)
                    {
                        string err = proc.StandardError.ReadToEnd();
                        AppendLog("Host", $"Net use failed: {err}");
                        return null;
                    }
                }
                File.Copy(uncPath, localFile, true);
                var lines = File.ReadAllLines(localFile);
                foreach (var line in lines)
                {
                    if (line.StartsWith("Key="))
                        return line.Substring(4);
                }
                return null;
            }
            catch (Exception ex)
            {
                AppendLog("Host", $"Error reading key from share: {ex.Message}");
                return null;
            }
            finally
            {
                RunCommandAndLog("net", $"use \\\\{targetIP}\\{SHARE_NAME} /delete", out _, out _);
                if (File.Exists(localFile)) File.Delete(localFile);
            }
        }

