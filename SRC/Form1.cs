        private void PopulateLocalIPs()
        {
            cmbHostIPs.Items.Clear();
            var ips = NetworkInterface.GetAllNetworkInterfaces()
                .Where(nic => nic.OperationalStatus == OperationalStatus.Up && nic.NetworkInterfaceType != NetworkInterfaceType.Loopback)
                .SelectMany(nic => nic.GetIPProperties().UnicastAddresses)
                .Where(addr => addr.Address.AddressFamily == System.Net.Sockets.AddressFamily.InterNetwork)
                .Select(addr => addr.Address.ToString())
                .Distinct()
                .ToList();

            if (ips.Count == 0)
            {
                cmbHostIPs.Items.Add("No IPv4 address found");
            }
            else
            {
                foreach (var ip in ips)
                    cmbHostIPs.Items.Add(ip);
                if (cmbHostIPs.Items.Count > 0)
                    cmbHostIPs.SelectedIndex = 0;
            }
        }

        private string FindWinDbgPath()
        {
            string[] possiblePaths = {
                @"C:\Program Files (x86)\Windows Kits\10\Debuggers\x64\windbg.exe",
                @"C:\Program Files (x86)\Windows Kits\10\Debuggers\x86\windbg.exe",
                @"C:\Program Files\Windows Kits\10\Debuggers\x64\windbg.exe",
                @"C:\Program Files\Windows Kits\10\Debuggers\x86\windbg.exe"
            };
            foreach (var path in possiblePaths)
            {
                if (File.Exists(path))
                    return path;
            }
            var which = Which("windbg.exe");
            if (which != null)
                return which;
            return null;
        }

        private string Which(string cmd)
        {
            var paths = Environment.GetEnvironmentVariable("PATH").Split(Path.PathSeparator);
            foreach (var path in paths)
            {
                var fullPath = Path.Combine(path, cmd);
                if (File.Exists(fullPath))
                    return fullPath;
            }
            return null;
        }

        private void btnLaunchWinDbg_Click(object sender, EventArgs e)
        {
            string port = txtHostPort.Text.Trim();
            string key = txtHostKey.Text.Trim();

            if (string.IsNullOrEmpty(port) || !int.TryParse(port, out _))
            {
                MessageBox.Show("Enter a valid port number.", "Missing Port", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }
            if (string.IsNullOrEmpty(key) || !Regex.IsMatch(key, @"^[A-Za-z0-9]+(\.[A-Za-z0-9]+){3}$"))
            {
                MessageBox.Show("Enter a valid KDNET key (four alphanumeric segments separated by dots).", "Invalid Key", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            string windbg = FindWinDbgPath();
            if (windbg == null)
            {
                MessageBox.Show("WinDbg not found. Please install Windows SDK/Debugging Tools.", "Missing WinDbg", MessageBoxButtons.OK, MessageBoxIcon.Error);
                return;
            }

            string args = $"-k net:port={port},key={key}";
            AppendLog("Host", $"Launching: {windbg} {args}");
            try
            {
                Process.Start(new ProcessStartInfo { FileName = windbg, Arguments = args, UseShellExecute = true });
            }
            catch (Exception ex)
            {
                MessageBox.Show($"Failed to launch WinDbg: {ex.Message}", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

