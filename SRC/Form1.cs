        private bool IsBitLockerEnabled()
        {
            try
            {
                var psi = new ProcessStartInfo
                {
                    FileName = "manage-bde",
                    Arguments = "-status C:",
                    RedirectStandardOutput = true,
                    UseShellExecute = false,
                    CreateNoWindow = true
                };
                using (var proc = Process.Start(psi))
                {
                    string output = proc.StandardOutput.ReadToEnd();
                    proc.WaitForExit();
                    return output.Contains("Protection On") || output.Contains("Protection Status: On");
                }
            }
            catch
            {
                return false;
            }
        }

        private void DisableBitLocker()
        {
            string bootDrive = GetBootDrive();
            var result = MessageBox.Show(
                $"This will fully disable BitLocker on drive {bootDrive}.\n\n" +
                "After you finish debugging, you must manually re-enable BitLocker using:\n" +
                "  Manage-bde -protectors -enable {drive}:\n" +
                "or via the BitLocker Control Panel.\n\n" +
                "Do you want to continue?",
                "Disable BitLocker",
                MessageBoxButtons.YesNo,
                MessageBoxIcon.Warning);

            if (result != DialogResult.Yes)
                return;

            AppendLog("Target", $"Disabling BitLocker on {bootDrive}... (this may take a while)");

            try
            {
                var psi = new ProcessStartInfo
                {
                    FileName = "powershell",
                    Arguments = $"-Command \"Disable-BitLocker -MountPoint '{bootDrive}'\"",
                    RedirectStandardOutput = true,
                    RedirectStandardError = true,
                    UseShellExecute = false,
                    CreateNoWindow = true
                };
                using (var proc = Process.Start(psi))
                {
                    string output = proc.StandardOutput.ReadToEnd();
                    string error = proc.StandardError.ReadToEnd();
                    proc.WaitForExit();
                    if (proc.ExitCode == 0)
                    {
                        AppendLog("Target", $"BitLocker disabled on {bootDrive}.");
                        MessageBox.Show($"BitLocker has been disabled on {bootDrive}.\n\n" +
                                        "To re-enable later, click 'Re-enable BitLocker' button or use Control Panel.",
                                        "BitLocker Disabled",
                                        MessageBoxButtons.OK,
                                        MessageBoxIcon.Information);
                    }
                    else
                    {
                        AppendLog("Target", $"Failed to disable BitLocker: {error}{output}");
                        MessageBox.Show($"Error disabling BitLocker:\n{error}{output}", "BitLocker Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                    }
                }
            }
            catch (Exception ex)
            {
                AppendLog("Target", $"Exception disabling BitLocker: {ex.Message}");
                MessageBox.Show($"Exception: {ex.Message}", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

        private void EnableBitLocker()
        {
            if (IsSecureBootEnabled())
            {
                var sbResult = MessageBox.Show(
                    "Secure Boot is currently enabled.\n\n" +
                    "BitLocker can be re-enabled with Secure Boot on. This is the normal configuration.\n\n" +
                    "Do you want to proceed with enabling BitLocker?",
                    "Secure Boot Detected",
                    MessageBoxButtons.YesNo,
                    MessageBoxIcon.Question);
                if (sbResult != DialogResult.Yes)
                    return;
            }
            else
            {
                var sbResult = MessageBox.Show(
                    "Secure Boot is NOT enabled.\n\n" +
                    "BitLocker can still be enabled, but it may be less secure.\n\n" +
                    "Do you want to continue?",
                    "Secure Boot Disabled",
                    MessageBoxButtons.YesNo,
                    MessageBoxIcon.Warning);
                if (sbResult != DialogResult.Yes)
                    return;
            }

            string bootDrive = GetBootDrive();
            AppendLog("Target", $"Re-enabling BitLocker on {bootDrive}... (this may take several minutes)");

            try
            {
                var psi = new ProcessStartInfo
                {
                    FileName = "powershell",
                    Arguments = $"-Command \"Enable-BitLocker -MountPoint '{bootDrive}' -SkipHardwareTest\"",
                    RedirectStandardOutput = true,
                    RedirectStandardError = true,
                    UseShellExecute = false,
                    CreateNoWindow = true
                };
                using (var proc = Process.Start(psi))
                {
                    string output = proc.StandardOutput.ReadToEnd();
                    string error = proc.StandardError.ReadToEnd();
                    proc.WaitForExit();
                    if (proc.ExitCode == 0)
                    {
                        AppendLog("Target", $"BitLocker re-enabled on {bootDrive}.");
                        MessageBox.Show($"BitLocker has been re-enabled on {bootDrive}.", "BitLocker Enabled", MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                    else
                    {
                        AppendLog("Target", $"Failed to enable BitLocker: {error}{output}");
                        MessageBox.Show($"Error enabling BitLocker:\n{error}{output}", "BitLocker Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
                    }
                }
            }
            catch (Exception ex)
            {
                AppendLog("Target", $"Exception enabling BitLocker: {ex.Message}");
                MessageBox.Show($"Exception: {ex.Message}", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }

        private bool IsSecureBootEnabled()
        {
            try
            {
