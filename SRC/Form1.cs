        private void btnEnableKDNET_Click(object sender, EventArgs e)
        {
            string hostIp = txtHostIp.Text.Trim();
            if (!IPAddress.TryParse(hostIp, out _))
            {
                MessageBox.Show("Enter a valid Host IP address (e.g., 192.168.1.50)", "Invalid IP", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            if (!int.TryParse(txtPort.Text, out int port) || port < 1 || port > 65535)
            {
                MessageBox.Show("Enter a valid port number (1-65535). Recommended: 50000-50039", "Invalid Port", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            string kdnetPath = FindKdnetPath(out string sourceDir);
            if (kdnetPath == null)
            {
                AppendLog("Target", "Cannot enable KDNET: kdnet.exe not found.");
                return;
            }

            string workDir = @"C:\KDNET";
            if (!Directory.Exists(workDir))
                Directory.CreateDirectory(workDir);

            string destKdnet = Path.Combine(workDir, "kdnet.exe");
            string sourceXml = Path.Combine(sourceDir, "VerifiedNICList.xml");
            string destXml = Path.Combine(workDir, "VerifiedNICList.xml");

            if (!File.Exists(destKdnet))
                File.Copy(kdnetPath, destKdnet, true);
            if (File.Exists(sourceXml) && !File.Exists(destXml))
                File.Copy(sourceXml, destXml, true);
            else if (!File.Exists(sourceXml))
                AppendLog("Target", "Warning: VerifiedNICList.xml not found in source directory. KDNET may fail.");

            AppendLog("Target", $"Running kdnet {hostIp} {port} ...");
            RunCommandAndLog(destKdnet, $"{hostIp} {port}", out string output, out string error);
            AppendLog("Target", output);
            if (!string.IsNullOrEmpty(error))
                AppendLog("Target", "ERROR: " + error);

            Regex keyRegex = new Regex(@"([A-Za-z0-9]+(?:\.[A-Za-z0-9]+){3})");
            Match match = keyRegex.Match(output);
            if (match.Success)
            {
                string key = match.Value;
                AppendLog("Target", $"*** Generated Key: {key} ***");
                MessageBox.Show($"Key generated:\n{key}\n\nCopy this key to the Host machine.", "KDNET Key", MessageBoxButtons.OK, MessageBoxIcon.Information);
                txtGeneratedKey.Text = key;
            }

            AppendLog("Target", "Enabling kernel debugging in BCD...");
            RunCommandAndLog("bcdedit", "/set {default} debug yes", out _, out string bcdError);
            if (!string.IsNullOrEmpty(bcdError))
                AppendLog("Target", "bcdedit error: " + bcdError);
            else
                AppendLog("Target", "Kernel debugging enabled successfully. Reboot the target machine to start debugging.");
        }

        private void btnDisableDebug_Click(object sender, EventArgs e)
        {
            AppendLog("Target", "Disabling kernel debugging...");

            // Turn off debug flag
            RunCommandAndLog("bcdedit", "/debug off", out string output1, out string error1);
            AppendLog("Target", output1);
            if (!string.IsNullOrEmpty(error1))
                AppendLog("Target", "Error (debug off): " + error1);

            // Remove debug value (may not exist, ignore)
            RunCommandAndLog("bcdedit", "/deletevalue {default} debug", out _, out string error2);
            if (!string.IsNullOrEmpty(error2) && !error2.Contains("not found"))
                AppendLog("Target", "Note: " + error2);

            // Clear all global KDNET settings from {dbgsettings}
            AppendLog("Target", "Clearing global KDNET settings...");
            RunCommandAndLog("bcdedit", "/deletevalue {dbgsettings} key", out _, out string errKey);
            RunCommandAndLog("bcdedit", "/deletevalue {dbgsettings} hostip", out _, out _);
            RunCommandAndLog("bcdedit", "/deletevalue {dbgsettings} port", out _, out _);
            RunCommandAndLog("bcdedit", "/deletevalue {dbgsettings} dhcp", out _, out _);
            RunCommandAndLog("bcdedit", "/deletevalue {dbgsettings} debugtype", out _, out _);
            
            if (!string.IsNullOrEmpty(errKey) && !errKey.Contains("not found"))
                AppendLog("Target", "Error clearing key: " + errKey);

            AppendLog("Target", "Debugging disabled. Reboot to return to normal operation.");
        }

