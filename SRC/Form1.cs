        private void CheckAdminRights()
        {
            if (!IsAdministrator())
            {
                var result = MessageBox.Show(
                    "This tool requires administrator privileges to configure kernel debugging.\n\n" +
                    "Click OK to restart as Administrator, or Cancel to exit.",
                    "Administrator Required",
                    MessageBoxButtons.OKCancel,
                    MessageBoxIcon.Warning);

                if (result == DialogResult.OK)
                {
                    RestartAsAdmin();
                }
                else
                {
                    Environment.Exit(0);
                }
            }
        }

        private bool IsAdministrator()
        {
            using (var identity = System.Security.Principal.WindowsIdentity.GetCurrent())
            {
                var principal = new System.Security.Principal.WindowsPrincipal(identity);
                return principal.IsInRole(System.Security.Principal.WindowsBuiltInRole.Administrator);
            }
        }

        private void RestartAsAdmin()
        {
            var processInfo = new ProcessStartInfo
            {
                UseShellExecute = true,
                FileName = Application.ExecutablePath,
                Verb = "runas"
            };
            try
            {
                Process.Start(processInfo);
            }
            catch (Exception ex)
            {
                MessageBox.Show($"Unable to elevate: {ex.Message}", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
            Environment.Exit(0);
        }

        #region Redist Detection

        private void CheckRedistFiles()
        {
            string arch = Environment.Is64BitOperatingSystem ? "x64" : "x86";
            string redistPath = Path.Combine(AppDomain.CurrentDomain.BaseDirectory, "Redist", arch);

            string kdnetCandidate = Path.Combine(redistPath, "kdnet.exe");
            string xmlCandidate = Path.Combine(redistPath, "VerifiedNICList.xml");

            if (File.Exists(kdnetCandidate) && File.Exists(xmlCandidate))
            {
                _redistKdnetPath = kdnetCandidate;
                _redistXmlDir = redistPath;
                AppendLog("Target", $"Using redistributed KDNET files from: {redistPath}");
            }
            else
            {
                _redistKdnetPath = null;
                _redistXmlDir = null;
                AppendLog("Target", "Redistributed KDNET files not found. Will fallback to Windows SDK or manual selection.");
            }
        }

        private string FindKdnetPath(out string xmlDirectory)
        {
            if (_redistKdnetPath != null && _redistXmlDir != null)
            {
                xmlDirectory = _redistXmlDir;
                return _redistKdnetPath;
            }

            string[] possiblePaths = {
                @"C:\Program Files (x86)\Windows Kits\10\Debuggers\x64\kdnet.exe",
                @"C:\Program Files (x86)\Windows Kits\10\Debuggers\x86\kdnet.exe",
                @"C:\Program Files\Windows Kits\10\Debuggers\x64\kdnet.exe",
                @"C:\Program Files\Windows Kits\10\Debuggers\x86\kdnet.exe"
            };

            foreach (var path in possiblePaths)
            {
                if (File.Exists(path))
                {
                    xmlDirectory = Path.GetDirectoryName(path);
                    return path;
                }
            }

            using (var dialog = new OpenFileDialog())
            {
                dialog.Title = "Locate kdnet.exe";
                dialog.Filter = "kdnet.exe|kdnet.exe";
                dialog.FileName = "kdnet.exe";
                if (dialog.ShowDialog() == DialogResult.OK)
                {
                    xmlDirectory = Path.GetDirectoryName(dialog.FileName);
                    return dialog.FileName;
                }
            }

            xmlDirectory = null;
            return null;
        }

